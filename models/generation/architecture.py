class ResnetBlock_adain(nn.Module):
    def __init__(self, fin, fout, activation=nn.ReLU(False), kernel_size=3):
        super().__init__()

        pw = (kernel_size - 1) // 2
        self.conv_block = nn.Sequential(
            nn.ReflectionPad2d(pw),
            nn.Conv2d(fin, fout, kernel_size=kernel_size),
            AdaptiveInstanceNorm2d(fout),
            activation,
            nn.ReflectionPad2d(pw),
            nn.Conv2d(fout, fout, kernel_size=kernel_size),
            AdaptiveInstanceNorm2d(fout)
        )

        self.learned_shortcut = (fin != fout)
        if self.learned_shortcut:
            self.conv_s = nn.Conv2d(fin, fout, kernel_size=1, bias=False)

    def forward(self, x):
        if self.learned_shortcut:
            x_s = self.conv_s(x)
        else:
            x_s = x
        y = self.conv_block(x)
        out = x_s + y
        return out


